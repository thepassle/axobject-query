const AXObjectsMap: TAXObjectQueryMap<
  TAXObjects,
  AXObjectName,
  AXObjectModelDefinition
> = {
  entries: function (): TAXObjects {
    return AXObjects;
  },
  forEach: function (
    fn: (
      values: AXObjectModelDefinition,
      key: AXObjectName,
      objects: TAXObjects,
    ) => void,
    thisArg: any = null,
  ): void {
    for (let [key, values] of AXObjects) {
      fn.call(thisArg, values, key, AXObjects);
    }
  },
  get: function (key: AXObjectName): AXObjectModelDefinition | undefined {
    const item = AXObjects.find((tuple) => tuple[0] === key);
    return item && item[1];
  },
  has: function (key: AXObjectName): boolean {
    return !!AXObjectsMap.get(key);
  },
  keys: function (): AXObjectName[] {
    return AXObjects.map(([key]) => key);
  },
  values: function (): AXObjectModelDefinition[] {
    return AXObjects.map(([, values]) => values);
  },
};

export default iterationDecorator(
  AXObjectsMap,
  AXObjectsMap.entries(),
) as TAXObjectQueryMap<TAXObjects, AXObjectName, AXObjectModelDefinition>;
